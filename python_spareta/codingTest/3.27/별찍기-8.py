# 첫째 줄부터 2×N-1번째 줄까지 차례대로 별을 출력한다.

# 입력: 5

# *        *  1 8 1
# **      **  2 6 2
# ***    ***  3 4 3
# ****  ****  4 2 4
# **********  5   5
# ****  ****
# ***    ***
# **      **
# *        *

n = int(input())

for i in range(1,n+1):
    print('*'* i + ' '* (2*n-2*i)+'*'* i)
for j in range(1,n):
    print('*'* (n-j)+ ' '*(2*j)+'*'* (n-j) )

    